#include <iostream>//2025.7.4 1200分
using namespace std;
int n, a[5005][5005];
int main()
{
    int t;
    cin >> t;
    while(t--)
    {
        cin >> n;
        for(int i = 1; i <= n; i++)
        {
            for(int j = 1; j <= n; j++)
            {
                a[i][j] = j;
            }
        }
        cout <<(2 * n - 3) << "\n";
        int op = 1, i;
        for(i = 1; i <= n - 3; i++)//3
        {
            cout << i << " 1 " <<  op + 1 << "\n";
            cout << i << " " << op + 2 << " " << n << "\n";
            op++;

        }
        cout << i << " 1 " << n - 1 << "\n";
        cout << i + 1 << " 1 " << n << "\n";
        cout << i + 2 << " 2 " << n << "\n";
    }
    return 0;
}
